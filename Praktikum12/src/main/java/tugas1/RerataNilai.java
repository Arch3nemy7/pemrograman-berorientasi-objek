package tugas1;

public class RerataNilai {

    public int average(int bilangan1, int bilangan2) {
        return (bilangan1 + bilangan2) / 2;
    }
    
    public double average(double bilangan1, double bilangan2) {
        return (bilangan1 + bilangan2) / 2;
    }
    
    public int average(int bilangan1, int bilangan2, int bilangan3) {
        return (bilangan1 + bilangan2 + bilangan3) / 3;
    }
}
